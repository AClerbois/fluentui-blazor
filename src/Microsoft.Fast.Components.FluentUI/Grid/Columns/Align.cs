namespace Microsoft.Fast.Components.FluentUI.Grid;

/// <summary>
/// Describes alignment for a <see cref="FluentDataGrid{TGridItem}"/> column.
/// </summary>
public enum Align
{
    /// <summary>
    /// Justifies the content against the start of the container.
    /// </summary>
    Left,

    /// <summary>
    /// Justifies the content at the center of the container.
    /// </summary>
    Center,

    /// <summary>
    /// Justifies the content at the end of the container.
    /// </summary>
    Right,
}
